console.log(5 == 5)                   // Um número igual ao outro

console.log(5 != 5)                   // Diferença de valor

console.log(10 > 7)                   // Maior que

console.log(3 < 5)                    // Menos que

console.log(7 >= 7)                   // Maior ou igual 

console.log(4 <= 9)                   // Menor ou igual

console.log(5 == 5 && 10 > 7)         // Um e o outro

console.log(5 == 4 && 10 > 7)         // Um e o outro

console.log(5 == 5 || 5 > 7)          // Um ou o outro

console.log(5 == 4 || 5 > 7)          // Um ou o outro
